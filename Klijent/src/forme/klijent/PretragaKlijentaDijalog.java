    private void btnIzaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiActionPerformed
      ModelTabeleKlijent mtk = (ModelTabeleKlijent) tabelaKlijenti.getModel();
      int red = tabelaKlijenti.getSelectedRow();
      if(red!=-1){
          Klijent k = mtk.vratiKlijenta(red);
          RentiranjeFrm rentfrm = (RentiranjeFrm) this.getParent();
          rentfrm.setKlijent(k);
          dispose();
      } else{
          JOptionPane.showMessageDialog(this, "Morate selektovati red!");
      }
    }//GEN-LAST:event_btnIzaberiActionPerformed

