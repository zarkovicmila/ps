    private void txtPrezimeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrezimeKeyReleased
        String ime = txtIme.getText().trim();
        String prezime = txtPrezime.getText().trim();
        Klijent k = new Klijent();
        if (!ime.isEmpty()) {
            k.setIme(ime);
        }
        if (!prezime.isEmpty()) {
            k.setPrezime(prezime);
        }

        try {
            ArrayList<Klijent> lista = Kontroler.getInstanca().pretraziKlijente(k);
            tabelaKlijenti.setModel(new ModelTabeleKlijent(lista));
        } catch (Exception ex) {
            Logger.getLogger(PretragaKlijentaDijalog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtPrezimeKeyReleased

